// Helper functions for filtering and searching jobs
export const getJobById = (id: number): Job | undefined => {
  return jobData.find((job) => job.id === id)
}

export const getJobsByDepartment = (department: string): Job[] => {
  return jobData.filter((job) => job.department.toLowerCase() === department.toLowerCase())
}

export const getJobsByLocation = (location: string): Job[] => {
  return jobData.filter((job) => job.location.toLowerCase().includes(location.toLowerCase()))
}

export const getJobsByType = (type: string): Job[] => {
  return jobData.filter((job) => job.type.toLowerCase() === type.toLowerCase())
}

export const searchJobs = (query: string): Job[] => {
  const lowercaseQuery = query.toLowerCase()
  return jobData.filter(
    (job) =>
      job.title.toLowerCase().includes(lowercaseQuery) ||
      job.department.toLowerCase().includes(lowercaseQuery) ||
      job.location.toLowerCase().includes(lowercaseQuery) ||
      job.description.toLowerCase().includes(lowercaseQuery),
  )
}

export const getDepartments = (): string[] => {
  return [...new Set(jobData.map((job) => job.department))]
}

export const getLocations = (): string[] => {
  return [...new Set(jobData.map((job) => job.location))]
}

export const getJobTypes = (): string[] => {
  return [...new Set(jobData.map((job) => job.type))]
}
